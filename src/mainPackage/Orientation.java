package mainPackage;

/**
 * Orientation enum represents the four cardinal directions: EAST, NORTH, WEST,
 * SOUTH. The main difference between Direction enum end Orientation enum is
 * that Direction is relative to something and Orientation is an absolute value
 * (NORTH represents up direction of the screen).
 */
public enum Orientation {
	EAST, NORTH, WEST, SOUTH;
}
